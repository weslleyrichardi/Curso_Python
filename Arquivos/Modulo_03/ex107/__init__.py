def aumentar(valor, porcem):
    result = valor + (valor * porcem / 100)
    mais_taxa = result
    return mais_taxa


def diminuir(valor, porcem):
    result = valor - (valor * porcem / 100)
    menos_taxa = result
    return menos_taxa


def dobro(num):
    result = 2 * num
    dobro = result
    return dobro
    
    
def metade(num):
    result = num / 2
    metade = result
    return metade
    
    
    